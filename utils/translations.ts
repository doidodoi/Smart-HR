
export type Language = 'lo' | 'en' | 'th';

export const getNameTitle = (gender: string | undefined, lang: Language): string => {
  const g = (gender || '').trim().toLowerCase();
  if (lang === 'lo') {
    if (g === 'male') return 'ທ້າວ';
    if (g === 'female') return 'ນາງ';
  } else if (lang === 'th') {
    if (g === 'male') return 'นาย';
    if (g === 'female') return 'นางสาว';
  } else {
    if (g === 'male') return 'Mr.';
    if (g === 'female') return 'Ms.';
  }
  return '';
};

